#include "libft.h"

int		ft_countwords(const char *s, char c)
{
	int	i;
	int	cp;

	i = 0;
	cp = 0;
	while (s[i] != 0)
	{
		if (s[i] != c)
		{
			cp++;
			while (s[i] != c && s[i] != 0)
				i++;
		}
		if (s[i] != 0)
			i++;
	}
	return (cp);
}
